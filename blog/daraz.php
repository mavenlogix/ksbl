                    <div class="blog-detail">
                        <p>With the aim of further empowering Daraz&#39; community of 30,000 marketplace sellers and enabling thousands of small-and-medium enterprises (SMEs) across the country to seamlessly transition towards ecommerce operations, Karachi School of Business and Leadership (KSBL) has partnered up with Daraz University (DU) &#45; an online education portal.</p>
                        <p>Through a collaboration with Daraz, KSBL will be providing a growing seller community exclusive access to specialized courses under this program. The collaboration between KSBL and Daraz is aimed at helping businesses tap their true potential. Over the course of the next few months, several other courses designed by KSBL will be made available to sellers through DU.</p>
                        <p>The program offered by KSBL is designed to equip Daraz vendors with skills that are essential to be a successful business leader and comprises of several courses across multiple disciples. The program is carefully developed in context of Pakistan and keeping the needs of small and medium enterprises in mind. It contains video lectures, animated illustrations, real life examples, webinars and case studies among others. At the end of each course, the seller would be tested via an online multiple choice questions test and only after securing 80 percent would the seller be eligible to get the certification. It enriches the leadership capacity, team working abilities and problem solving skills of individuals taking this course.</p>
                        <p>"At KSBL, our purpose is to develop leaders who can make meaningful contributions to society, through consistently ethical decision-making, knowledge, and humility. We believe that people are the only enduring source of competitive advantage. We further believe that the best way to invest in people is to provide them with a framework which enables and accelerates their development into leaders. To provide such a framework, KSBL will help develop character and capability of Daraz&#39; community of marketplace sellers; we believe these will, in turn, lead to consistently ethical decision-making, knowledge, and humility," said Mr. Mubashar Hameed, Rector KSBL.</p>
                        <p>"Our objective is to engage, educate and empower the community of marketplace sellers. Education has always remained an area of special focus at Daraz and it is exciting to have found an amazing partner like KSBL to join us in the mission of upskilling our seller community and the business community at large. We are dedicated towards creating opportunities of growth not only for our existing sellers but also for those business owners who chose to transition online and start their ventures with us amidst the new normal of the COVID19 pandemic," said Syed Faraz Ali, Regional Head of Marketplace, Daraz.</p>
