                        <p>KSBL held presentations on Media Buying and Planning delivered by #EMBA students. The session was co-facilitated by Mr. @Taha Khan (Associate Director, @Mindshare) and our alumnus @Syed Sohaib Ul Hassan (Deputy Manager Brands, Amreli Steels Limited). A panel of dignitaries was also invited from marketing field, to give their expert opinion on performance of participants.</p>
