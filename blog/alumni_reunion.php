                            <h2>KSBL Organized Alumni Reunion 2020</h2>
                            <div class="post-date">Monday, February 17th, 2020</div>
                        </div>
                    </div>
                    <div class="blog-detail">
                        <p>The KSBL Alumni from batches of 2014 to 2019 were given a warm welcome at the Grand Alumni Reunion 2020, by the Faculty, management and current students. The evening was filled with recalling memories, sharing new initiatives and celebrating accomplishments of the Institute, Alumni and current students. Loads of Laughter, excitement and new memories were created, to be cherished in the coming years.</p>
                        <p>At the beginning of session, Dr. Zeeshan Ahmed (Dean, KSBL) welcomed graduates and shared latest initiatives that have been launched under the umbrella of KSBL. He highlighted the establishment and success of Urban Youth Project (UYP) and other strengthening partnerships. He announced that in upcoming months KSBL will soon become an entrepreneurial centre with new initiatives in the pipeline. In addition to that he also mentioned new members on the KSBL board, like Ms. Seema Kamil (CEO, UBL) and Ms. Tania Aidrus (Head of Digital Economy, Pakistan), to name a few. The Dean invited the high achiever Alumni along with current students who won prestigious competitions recently and in the past, to celebrate and recognize their hard work, specifically the CFA competitions winning streak.</p>
                        <p>Later Dr. Jawaid Abdul Ghani (Professor of Strategy & Market Research) and Dr. Muhammad Athar Siddiqui (Associate Dean, KSBL) recalled their class memories with Alumni and congratulated them for their achievements, in professional life.</p>
                        <p>The Alumni also invited faculty on stage for a light-hearted conversation to reminisce their old memories with their mentors. The activities were followed by elections conducted for the Alumni Council, where all nominated prospects delivered their elevator pitches.</p>
                        <p>The event concluded with exciting photo sessions of all batches, and dinner at which alumni reconnected and caught up with happenings in each other's lives.</p>
