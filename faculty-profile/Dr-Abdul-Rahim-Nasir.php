                            <p><span style="color: #ff9f19;">Prof. Abdul-Rahim Ahmad</span> is an award-winning academician and an active researcher of global repute working at the Department of Decision Sciences, Karachi School of Business Leadership, where he is involved in leading-edge interdisciplinary teaching, research, curricula development, and academic service. He is also an adjunct professor at the department of Systems Design Engineering, University of Waterloo, Canada, where he is involved in graduate student thesis supervision and cutting-edge interdisciplinary research. He has served in top universities in Canada (University of Waterloo, Wilfrid Laurier University, Dalhousie University), Saudi Arabia (KFUPM), and Pakistan (KSBL) for several decades.</p>
                            <p>Dr. Ahmad’s research interests are mainly in the areas of Knowledge-based Decision Support Systems (DSS), Expert System (ES), and Decision Modeling with applications in Supply Chain Operations Management (SCM), Layout Optimization as well as empirical research in Strategic Management of Technology. Dr. Ahmad has more than 50 publications in high-repute and high-impact journals and conference proceedings. He has also regularly acted as reviewer for such reputable journals as EJOR, IJPR, IJOQM, etc. as well as international conferences. He has worked on several government- and industry-funded collaborative research projects in Canada and Saudi Arabia. </p>
                            <p>Dr. Ahmad’s current teaching interests are in the areas of Business Strategy, Business and Supply Chain Analytics, Information Systems Strategy, Management Science, etc. at both undergraduate and graduate levels. He is firm believer in the power of active, interactive, and cooperative learning in developing effective leaders and lifelong learners who can be agents of positive change in organizations and societies. He perceives teaching and learning a mutually beneficial learning enterprise between instructor and learners. Dr. Ahmad’s extensive award winning teaching experience at several AACSB accredited business schools in Canada and Saudi Arabia is considered asset for his peers and students. He has received more than 10 distinguished teaching awards from the University of Waterloo (Canada), Dalhousie University (Canada), King Fahd University of Petroleum and Minerals (Saudi Arabia), and Umm Al-Qura University (Saudi Arabia).</p>
                            <p>Dr. Ahmad also has significant industrial and consulting experience in Supply Chain Management, Decision Support Systems, and Software Development. Consequently, he is familiar with the workplace social, ethical, and organizational issues as well as various market dynamics, which all prove assets in his passionate and interactive teaching through the use of contemporary real world examples. Furthermore, he has excellent individual and team-based project management skills acquired through his participation in a variety of academic, research, and industrial ventures. More importantly, he discerns the difference between creativity and productivity and he is cognizant of the value of both in academic settings and scientific inquiries.</p>
                            <p>Dr. Ahmad regularly consults with industry and academia. His consultations with Higher Education Institutions span Curricula Development, Curricula Review, Faculty Development, Academic Accreditation, and Continuous Quality Improvement. He is an academic accreditation consultant for universities pursuing AACSB, EFMD/EPAS, and NCAAA/EEC-HES accreditation and re-accreditation. He offers executive development courses in Strategic Management, Information Systems, and Project Management. He is a long time professional member of the Institute for Operations Research and Management Sciences (INFORMS) the Institute of Electrical and Electronics Engineers (IEEE), the Association for Computing Machinery (ACM), IEEE Technology and Management Society (TEMS), and the Association of Information Systems (AIS). </p>
